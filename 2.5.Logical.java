class Logical {
    public static void main(String[] args) 
    {
        System.out.println("\nLogical Operator\n");
                
        boolean x = true; 
        boolean y = false; 
               
                
        System.out.println( "Value of x = " + x); 
        System.out.println( "Value of y = " + y); 
                
        System.out.println("x && y: " + (x && y)); 
        System.out.println("x || y: " + (x || y)); 
        System.out.println("!x: " + (!x));  
    }
}
