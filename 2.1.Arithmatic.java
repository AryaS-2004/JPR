class Arithmatic 
{ 
    public static void main (String[] args) 
    { 
        System.out.println("\nArithmatic Operator\n");

        int add = 2 + 4; 
        int sub = 10 - 3;
        int div = 6 / 2; 
        int mul = 5 * 5; 
        int mod = 7 % 2; 
        
        System.out.println("Addition : 2 + 4 = " + add); 
        System.out.println("Subtraction : 10 - 3 = " + sub); 
        System.out.println("Division : 6 / 2 = " + div); 
        System.out.println("Muitiplication : 5 * 5 = " + mul); 
        System.out.println("Modulus : 7 % 2 = " + mod); 
    } 
}

